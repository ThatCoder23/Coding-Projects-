# Sixth Decimal Division Program
# This program will divide two numbers that are inputted in this

# program greeting
print('Hello this program divides two numbers to the sixth decimal point')

# prompt the user for two numbers
num_1 = int(input('Enter your first number:'))
num_2 = int(input('Enter your second number:'))

# divide the two numbers
num_3 = num_1 / num_2

# display result
print('Your answer is:', format(num_3, '.6f'))
